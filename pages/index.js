import Head from "next/head"
import Nav from "../components/NavBar"
import Jumbotron from "../components/Jumbotron"
import Search from "../components/Search"
import { useState, useEffect } from "react"
import { useGetTrendingTitlesQuery } from "../features/TMDB_API/moviesSlice"

export default function Home() {
  const [searchBar, setSearchBarState] = useState(false)

  const onKeyDown = e => {
    if ((e.metaKey && e.key === "k") || (e.metaKey && e.key === "K")) {
      setSearchBarState(true)
    }
  }

  useEffect(() => {
    document.addEventListener("keydown", onKeyDown)

    return () => {
      document.removeEventListener("keydown", onKeyDown)
    }
  }, [])

  const { data: movieData } = useGetTrendingTitlesQuery({
    type: "all",
    time_window: "week",
    page: "1",
  })

  return (
    <div className="bg-gradient-to-br from-violet-900 to-fuchsia-800">
      <Head>
        <title>TDW MP2 Pedro Miguel Pereira</title>
        <meta name="description" content="Generated by create next app" />
        <link rel="icon" href="/favicon.ico" />
      </Head>

      <div className="bg-black bg-opacity-40">
        {searchBar && <Search setSearchBarState={setSearchBarState} />}
        <Nav setSearchBarState={setSearchBarState} />
        <Jumbotron movieData={movieData} />
        <div className="h-96 ">asdasdsa</div>
        <div className="h-96 ">sdsads</div>
        <div className="h-96 "></div>
      </div>
    </div>
  )
}
